     * Display the specified resource.
     *
     * @param  \App\Models\Grupo  $grupo
     * @return \Illuminate\Http\Response
     */
    public function show(Grupo $grupo)
    {
        $this->authorize('view', $grupo);

        $alumno = auth()->user()->alumno;

        $matricula = Matricula::where('grupo_id', $grupo->id)->where('alumno_id', $alumno->id)->first();
        $alumno_unidades = Alumno_unidade::where('matricula_id', $matricula->id)->get();

        return view('student.grupos.show', compact('alumno_unidades', 'matricula'));
    }

